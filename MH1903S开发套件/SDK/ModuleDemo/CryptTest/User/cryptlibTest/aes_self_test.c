void AES_Rand_Test(void)
{
	uint8_t i, j;
	uint32_t t;
	mh_pack_mode_def modes[2] = { ECB, CBC};
	unsigned char s[][10] = {"ECB", "CBC"};
    mh_rng_callback f_rng = mh_rand_p;
	
	mh_aes_key_size_def au32Keysize[3] = {128, 192, 256};
	
	uint8_t au8Plain[AES_PLAN_LEN] = {0};
	uint8_t au8Key[256] = {0x00};
	uint8_t au8Iv[16] = {0x00};
	uint8_t au8Cipher[AES_PLAN_LEN] = {0x00};
	uint8_t au8Mplain[AES_PLAN_LEN] = {0x00};
	
	uint32_t u32PlainLen = 0;
	
	mh_rand(&u32PlainLen, sizeof(u32PlainLen));
	u32PlainLen &= ((AES_PLAN_LEN - 1) & ~(0x0F));		//16Bytes*n
	
	mh_rand(au8Plain, u32PlainLen);
	mh_rand(au8Iv, sizeof(au8Iv));
	
	for(i = 0; i < 3; i ++)
	{
		mh_rand(au8Key, au32Keysize[i]>>3);
		
		for(j = 0; j < 2; j ++)
		{
			memset(au8Mplain, 0, sizeof(au8Mplain));
			memset(au8Cipher, 0, sizeof(au8Cipher));
			mh_aes_enc(modes[j], au8Cipher, sizeof(au8Cipher), au8Plain, u32PlainLen, au8Key, au32Keysize[i], au8Iv, f_rng, NULL);
			mh_aes_dec(modes[j], au8Mplain, sizeof(au8Mplain), au8Cipher, u32PlainLen, au8Key, au32Keysize[i], au8Iv, f_rng, NULL);
			t = (!memcmp(au8Mplain, au8Plain, u32PlainLen));
			r_printf(t, "AES RNG ");
			printf("%s Key%d Test.\n", s[j], au32Keysize[i]);
		}
	}
	
}
